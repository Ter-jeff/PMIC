namespace SpreedSheet.Core.Enum
{
    /// <summary>
    ///     Text-wrap mode of cell
    /// </summary>
    public enum TextWrapMode
    {
        /// <summary>
        ///     No break (default)
        /// </summary>
        NoWrap,

        /// <summary>
        ///     Normal word break
        /// </summary>
        WordBreak,

        /// <summary>
        ///     Break enabled for all characters
        /// </summary>
        BreakAll
    }
}